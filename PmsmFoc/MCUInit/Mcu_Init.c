/**
 * \brief Configure GPIO pins for LED control.
 *
 * \details
 * This function initializes the GPIO pins connected to LEDs and sets their
 * initial state to high.
 */
static void PmsmFoc_initLeds(void)
{
    /* Configure test pins */
    IfxPort_setPinModeOutput(&MODULE_P13, 0, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);   /* Set Pin13_0 as GP-Out */
    IfxPort_setPinModeOutput(&MODULE_P13, 1, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);   /* Set Pin13_1 as GP-Out */
    IfxPort_setPinModeOutput(&MODULE_P13, 2, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);   /* Set Pin13_2 as GP-Out */
    IfxPort_setPinModeOutput(&MODULE_P13, 3, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);   /* Set Pin13_3 as GP-Out */


    IfxPort_setPinMode(&MODULE_P13, 0, IfxPort_Mode_outputPushPullGeneral); /* LED107 */
    IfxPort_setPinHigh(&MODULE_P13, 0);

    IfxPort_setPinMode(&MODULE_P13, 1, IfxPort_Mode_outputPushPullGeneral); /* LED108 */
    IfxPort_setPinHigh(&MODULE_P13, 1);

    IfxPort_setPinMode(&MODULE_P13, 2, IfxPort_Mode_outputPushPullGeneral); /* LED109 */
    IfxPort_setPinHigh(&MODULE_P13, 2);

    IfxPort_setPinMode(&MODULE_P13, 3, IfxPort_Mode_outputPushPullGeneral); /* LED110 */
    IfxPort_setPinHigh(&MODULE_P13, 3);
}

/******************************************************************************/
/*-----------------------------Public Functions-------------------------------*/
/******************************************************************************/

void PmsmFoc_initHardware(MotorControl* const motorCtrl)
{
    IfxCpu_disableInterrupts();

    /* Initialize GPIO pins for LEDs */
    PmsmFoc_initLeds();

    /* Initialize SPI interfaces */
    PmsmFoc_Qspi_initQspi();

    /* Load power management and WDT configurations */
    {
        IfxCpu_enableInterrupts();

        /* Initialize the TLF35584 power management IC */
        PmsmFoc_Tlf35584_Init();
        IfxTLF35584_unprotectRegister();
        IfxTLF35584_disableWindowWatchdog();
        IfxTLF35584_disableErrPinMonitor();
        IfxTLF35584_protectRegister();
        IfxTLF35584_gotoNormalState();

        IfxCpu_disableInterrupts();
    }

    /* Initialize time constants for time functions (bsp.c) */
    initTime();

    /* Initialize the TLE9180 motor driver IC */
    PmsmFoc_Tle9180_Init();

    /* Initialize GTM driver */
    PmsmFoc_Gtm_initGtm(&motorCtrl->inverter);

    /* Initialize the EVADC driver */
    PmsmFoc_Evadc_initEvadc(&motorCtrl->inverter);

    /* Initialize the position sensor driver */
    PmsmFoc_PositionAcquisition_init(&motorCtrl->positionSensor, PositionAcquisition_SensorType_Encoder);

    /* Send startup configuration to the TLE9180 */
    IfxCpu_enableInterrupts();
    PmsmFoc_Tle9180_loadConfiguration();
    IfxCpu_disableInterrupts();
}
