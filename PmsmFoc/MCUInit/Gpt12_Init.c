/**
 * \brief Initialize the GPT12 module for incremental encoder functionality.
 *
 * \param[in] gpt12IncrEnc Pointer to the GPT12 incremental encoder configuration structure.
 *
 * \details
 * This function configures the GPT12 module for incremental encoder functionality.
 * It sets up the clock prescalers, sensor parameters, speed acquisition thresholds,
 * interrupt priorities, and other hardware resources required for position and speed
 * acquisition in PMSM FOC motor control.
 */
void PmsmFoc_Gpt12_initGpt12(IfxGpt12_IncrEnc* gpt12IncrEnc)
{
    /* Enable GPT12 module */
    IfxGpt12_enableModule(&MODULE_GPT120);

    /* Set global clock prescalers */
    IfxGpt12_setGpt1BlockPrescaler(&MODULE_GPT120, IfxGpt12_Gpt1BlockPrescaler_8);
    IfxGpt12_setGpt2BlockPrescaler(&MODULE_GPT120, IfxGpt12_Gpt2BlockPrescaler_4);

    /* Configure the GPT12 incremental encoder */
    IfxGpt12_IncrEnc_Config gpt12Config;
    IfxGpt12_IncrEnc_initConfig(&gpt12Config, &MODULE_GPT120);

    /* Sensor configuration */
    gpt12Config.base.offset = 0; /* Offset set to zero */
    gpt12Config.base.reversed = ENCODER_REVERSED; /* Reverse signal configuration */
    gpt12Config.base.resolution = ENCODER_RESOLUTION; /* Encoder resolution */
    gpt12Config.base.resolutionFactor = IfxStdIf_Pos_ResolutionFactor_fourFold;

    /* Speed configuration */
    gpt12Config.base.speedModeThreshold = ENCODER_SPEED_MODE_THRESHOLD;
    gpt12Config.base.minSpeed = ENCODER_BASE_MIN_SPEED;
    gpt12Config.base.maxSpeed = ENCODER_BASE_MAX_SPEED;
    gpt12Config.base.speedFilterEnabled = TRUE;
    gpt12Config.base.speedFilerCutOffFrequency = 1000;

    /* Interrupt priorities */
    gpt12Config.base.updatePeriod = ENCODER_UPDATE_PERIOD;
    gpt12Config.zeroIsrPriority = INTERRUPT_PRIORITY_ENCODER_GPT12;
    gpt12Config.zeroIsrProvider = ENCODER_GPT12_HOST_CPU;

    /* Hardware resource configuration */
    gpt12Config.pinA = ENCODER_GPT12_PIN_A;
    gpt12Config.pinB = ENCODER_GPT12_PIN_B;
    gpt12Config.pinZ = ENCODER_GPT12_PIN_Z;
    gpt12Config.pinDriver = IfxPort_PadDriver_cmosAutomotiveSpeed3;

    /* Initialize the incremental encoder */
    IfxGpt12_IncrEnc_init(gpt12IncrEnc, &gpt12Config);

    /* Configure timer T2 for overflow handling */
    sint32 resolution = gpt12IncrEnc->resolution - 1;
    IfxGpt12_T2_setMode(&MODULE_GPT120, IfxGpt12_Mode_reload);
    IfxGpt12_T2_setTimerValue(&MODULE_GPT120, (uint16) resolution);
    IfxGpt12_T2_setReloadInputMode(&MODULE_GPT120, IfxGpt12_ReloadInputMode_bothEdgesTxOTL);
}
