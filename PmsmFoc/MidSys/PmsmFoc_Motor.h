/**
 * \brief Motor parameter structure for PMSM motor control.
 * \details
 * This structure defines the electrical, magnetic, mechanical, and operational
 * parameters of a Permanent Magnet Synchronous Motor (PMSM). These parameters
 * are used for configuring and controlling the motor in a PMSM motor control system.
 */
typedef struct
{
    boolean winding;          /**< \brief Winding type:
                                   - `0`: Wye
                                   - `1`: Delta */
    boolean rotorSailency;    /**< \brief Rotor saliency type:
                                   - `0`: Round
                                   - `1`: Salient */
    uint8 polePairs;          /**< \brief Number of pole pairs. */
    float32 rs;               /**< \brief Stator resistance per phase (Ohm). */
    float32 ms;               /**< \brief Stator mutual inductance (H). */
    float32 lm;               /**< \brief Stator inductance fluctuation (H). */
    float32 ls;               /**< \brief Stator self-inductance per phase (H). */
    float32 l0;               /**< \brief Stator zero-sequence inductance (H). */
    float32 ld;               /**< \brief Stator d-axis inductance (H). */
    float32 lq;               /**< \brief Stator q-axis inductance (H). */
    float32 ldq;              /**< \brief Stator dq-axis inductance (H). */
    float32 ke;               /**< \brief Back EMF constant (V·s/rad). */
    float32 psiM;             /**< \brief Permanent magnet flux linkage (Wb). */
    float32 tc;               /**< \brief Torque constant (N·m/A). */
    float32 jm;               /**< \brief Total mechanical inertia (kg·m²). */
    float32 bm;               /**< \brief Viscous friction coefficient (N·m·s). */
    float32 iNom;             /**< \brief Nominal phase current (A). */
    float32 iMax;             /**< \brief Maximal phase current (A). */
    float32 uNom;             /**< \brief Nominal phase voltage (V). */
    float32 pNom;             /**< \brief Nominal power (W). */
    float32 pMax;             /**< \brief Maximal power (W). */
    float32 nNom;             /**< \brief Nominal speed (RPM). */
    float32 nMax;             /**< \brief Maximal speed (RPM). */
} MotorParameters;

