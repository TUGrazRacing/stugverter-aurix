void PmsmFoc_doVfControl(MotorControl* const motorCtrl)
{
#if (EMOTOR_LIB == MC_EMOTOR)
    CplxStdReal cossin; /**< Temporary variable for Sin and Cos values. */
#endif

    /* Update rotor position and speed. */
    (void)PmsmFoc_PositionAcquisition_updatePosition(&motorCtrl->positionSensor);
    (void)IfxStdIf_Pos_radsToRpm(PmsmFoc_PositionAcquisition_getSpeed(&motorCtrl->positionSensor));

    /* Reconstruct currents from ADC results. */
    PmsmFoc_reconstructCurrent(motorCtrl);

    if (motorCtrl->interface.start == TRUE)
    {
        /* Activate the inverter if the motor is running. */
        IfxTLE9180_activateEnable(&tle9180.driver);

        /* Update electrical angle and calculate modulation index. */
#if (EMOTOR_LIB == MC_EMOTOR)
        sint16 electricAngle;
        motorCtrl->openLoop.electricalAngle += motorCtrl->openLoop.electricalAngleDelta;
        electricAngle = (motorCtrl->openLoop.electricalAngle >> 16) & (COSSIN_TABLE_SIZE - 1);
        cossin = LookUp_CosinusSinus(electricAngle); /**< Calculate Sin and Cos of the angle. */
#endif
        motorCtrl->openLoop.modulationIndex.real = cossin.real * motorCtrl->openLoop.amplitude;
        motorCtrl->openLoop.modulationIndex.imag = cossin.imag * motorCtrl->openLoop.amplitude;
    }
    else
    {
        /* Reset electrical angle and modulation indices when stopped. */
        motorCtrl->openLoop.electricalAngle = 0;
        motorCtrl->openLoop.electricalAngleDelta = 0;
        motorCtrl->openLoop.amplitude = 0.0;
        motorCtrl->openLoop.modulationIndex.real = 0;
        motorCtrl->openLoop.modulationIndex.imag = 0;

        /* Deactivate the inverter. */
        IfxTLE9180_deactivateEnable(&tle9180.driver);
    }

    /* Perform Space Vector Modulation (SVM). */
    PmsmFoc_doSvPwmModulation(&motorCtrl->inverter, motorCtrl->openLoop.modulationIndex);

    /* Update the PWM duty cycle. */
    PmsmFoc_doPwmSvmUpdate(&motorCtrl->inverter);
}

