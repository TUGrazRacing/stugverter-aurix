/**
 * \brief Interface information for motor control.
 */
typedef struct
{
    uint32 running;                 /**< \brief 1 = Motor is running; 0 = Motor is stopped. */
    uint32 start;                   /**< \brief 1 = Request to start; 0 = No effect. */
    uint32 stop;                    /**< \brief 1 = Request to stop; 0 = No effect. */
    uint32 demo;                    /**< \brief 1 = Start demo; 0 = Stop demo. */
    sint32 motorTargetSpeed;        /**< \brief Target speed in RPM. */
    uint32 hadEmergency;            /**< \brief 1 = Emergency event occurred; 0 = No event. */
} Interfaces;

/**
 * \brief Open-loop control information for PMSM motors.
 */
typedef struct
{
    float32 amplitude;              /**< \brief Vector amplitude in open-loop mode (Range: [0, 1]). */
    uint32 electricalAngleDelta;    /**< \brief Delta angle for electrical position (Q16.16 format). */
    uint32 electricalAngle;         /**< \brief Electrical angle in open-loop mode. */
#if(EMOTOR_LIB == MC_EMOTOR)
    CplxStdReal AngleCosSin;        /**< \brief Sin and Cos values of the angle. */
    CplxStdReal modulationIndex;    /**< \brief Modulation index for SVM. */
#endif
} OpenLoop;

/**
 * \brief Field-Oriented Control (FOC) information.
 * \details
 * This structure contains parameters, states, and objects used for
 * Field-Oriented Control (FOC) in PMSM motor control systems.
 */
typedef struct
{
#if (EMOTOR_LIB == MC_EMOTOR)
    /* Phase Current and Voltage Information */
    T3Phase iPhaseMeas;             /**< \brief Measured phase currents (U, V, W). */
    T3Phase bemf;                   /**< \brief Measured Back-EMF values (U, V, W). */
    CplxStdReal iab;                /**< \brief Alpha/Beta values of current space vector. */
    CplxStdReal vabRef;             /**< \brief Reference Alpha/Beta values of voltage space vector. */

    /* DQ-Axis Current and Voltage Information */
    CplxStdReal idqRef;             /**< \brief Reference DQ-axis current space vector. */
    CplxStdReal idqRefExternal;     /**< \brief External DQ-axis current reference space vector. */
    CplxStdReal idqMeas;            /**< \brief Measured DQ-axis current space vector. */
    CplxStdReal idqError;           /**< \brief DQ-axis current error (reference - measured). */
    CplxStdReal vdqRef;             /**< \brief Reference DQ-axis voltage space vector. */
    StdReal vdqMag;                 /**< \brief Magnitude of voltage space vector. */

    /* Angular Information */
    CplxStdReal CurrentAngleCosSin; /**< \brief Sin and Cos values of the current angle. */
    CplxStdReal VoltageAngleCosSin; /**< \brief Sin and Cos values of the voltage angle. */
    sint16 electricalAngle;         /**< \brief Rotor angle (Encoder, Resolver, Estimator, etc.). */

    /* Torque Information */
    StdReal torqueRef;              /**< \brief Torque command or output of speed regulator. */
    StdReal torqueEst;              /**< \brief Estimated torque based on motor parameters and currents. */

    /* Control Objects */
    Ifx_PicF32 piId;                /**< \brief PI controller for the D-axis current. */
    Ifx_PicF32 piIq;                /**< \brief PI controller for the Q-axis current. */
    SpeedControl speedControl;      /**< \brief Speed control object. */

    /* Reference Ramps */
    Ifx_RampF32 speedRamp;          /**< \brief Speed reference ramp. */
    Ifx_RampF32 idRefExternalRamp;  /**< \brief External D-axis current reference ramp. */
    Ifx_RampF32 iqRefExternalRamp;  /**< \brief External Q-axis current reference ramp. */

    /* Modulation Information */
    CplxStdReal modulationIndex;    /**< \brief Modulation index for Space Vector Modulation (SVM). */

#endif /* (EMOTOR_LIB == MC_EMOTOR) */
} PmsmFoc;

/**
 * \brief Control parameters for PMSM motor control.
 * \details
 * This structure contains various control parameters, counters, and flags
 * for managing motor operation and calibration in PMSM motor control systems.
 */
typedef struct
{
    /* Motor Control State and Scheme */
    uint32 state;                  /**< \brief Motor state (e.g.: V/f, FOC). */
    ControlScheme controlScheme;   /**< \brief Control scheme (e.g., speed control, current control). */

    /* Motor Rotation and Inverter Status */
    uint16 rotationDir;            /**< \brief Rotation direction of motor:
                                         - Increasing rotor angle
                                         - Decreasing rotor angle. */
    uint16 inverterStatus;         /**< \brief 0 -> Inverter disabled, 1 -> Inverter enabled. */

    /* General Purpose Counters */
    uint32 counter;                /**< \brief General-purpose counter. */
    uint32 rampCounter;            /**< \brief Counter for motor speed ramp up/down. */
    uint32 alignmentCounter;       /**< \brief Counter for rotor initial positioning / alignment in V/F. */
    uint32 nonRealTimeCounter;     /**< \brief Counter for non-real-time tasks. */

    /* Calibration Counters */
    uint32 encOffsetCalCounter;    /**< \brief Counter for encoder offset calibration. */
    uint32 encTopZeroCounter;      /**< \brief Counter for encoder top zero calibration. */
    uint32 tuneCurrentRegsCounter; /**< \brief Counter for current regulator calibration. */

    /* Calibration and Tuning Flags */
    boolean tuneCurrentRegs;       /**< \brief Current regulator tuning flag:
                                         - `TRUE`: Tuning enabled.
                                         - `FALSE`: Tuning disabled. */
} ControlParameters;

/**
 * \brief Diagnostic parameters for PMSM motor control.
 * \details
 * This structure contains various diagnostic parameters, flags, and settings
 * used to monitor and configure the motor control system.
 */
typedef struct
{
    /* Motor Phase and Rotation Settings */
    boolean swapped;                        /**< \brief Indicates if phases A and B are swapped:
                                            - `FALSE`: ABC sequence.
                                            - `TRUE`: ACB sequence (used in @ref DIAG mode). */
    uint16 rotate;                          /**< \brief Phase rotation pattern (range: [0, 2]):
                                            - Swapped = `FALSE`: 0 (ABC), 1 (CAB), 2 (BCA).
                                            - Swapped = `TRUE`: 0 (ACB), 1 (BAC), 2 (CBA). */

    /* Control Enable Flags */
    boolean focControlEnabled;              /**< \brief Specifies if Field-Oriented Control (FOC) is enabled:
                                            - `TRUE`: FOC is enabled.
                                            - `FALSE`: FOC is disabled. */
    boolean encoderEnabled;                 /**< \brief Indicates if the position sensor (encoder) is enabled:
                                            - `TRUE`: Encoder is enabled.
                                            - `FALSE`: Encoder is disabled. */

    /* Diagnostic and Calibration Settings */
    uint16 maxTurn;                         /**< \brief Maximum number of turns allowed before stopping
                                            (used in @ref DIAG mode). */
    sint32 electricalAngleOffset;           /**< \brief Electrical angle offset for calibration. */
    sint32 electricalAngleSensor;           /**< \brief Current electrical angle from the position sensor. */
    boolean encCalStatus;                   /**< \brief Encoder calibration status:
                                            - `TRUE`: Calibration completed.
                                            - `FALSE`: Calibration not completed. */
} Diagnostic;

/**
 * \brief Motor control structure for PMSM FOC.
 * \details
 * Contains all the parameters, states, and objects required for Field-Oriented Control.
 */
typedef struct
{
    Interfaces interface;                  /**< \brief Interface parameters. */
    ControlParameters controlParameters;   /**< \brief Control parameters object. */
    Diagnostic diagnostic;                 /**< \brief Diagnostic object. */
    Inverter inverter;                     /**< \brief Inverter object. */
    PmsmFoc pmsmFoc;                       /**< \brief FOC object. */
    OpenLoop openLoop;                     /**< \brief Open loop object. */
    PositionAcquisition positionSensor;    /**< \brief Position sensor object. */
    MotorParameters motor;                 /**< \brief Motor parameters object. */
} MotorControl;

