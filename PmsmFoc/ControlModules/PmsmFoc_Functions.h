/**
 * \brief State machine states for motor control.
 * \details
 * Enumerates the possible states of the PMSM motor control state machine.
 */
typedef enum StateMachine
{
    StateMachine_calibration = 0,        /**< Calibration state. */
    StateMachine_focClosedLoop,         /**< Field-Oriented Control closed-loop mode. */
    StateMachine_enableInverter,        /**< Enable inverter state. */
    StateMachine_vfOpenLoop,            /**< Open-loop V/F control state. */
    StateMachine_tuneCurrentRegulators, /**< Current regulator tuning state. */
    StateMachine_prePositioning,        /**< Pre-positioning state for rotor alignment. */
    StateMachine_motorIdle,             /**< Motor idle state. */
    StateMachine_motorStop,             /**< Motor stopped state. */
    StateMachine_motorHold,             /**< Motor hold state. */
    StateMachine_identifyMotorParameters /**< Identify motor parameters state. */
} StateMachine;

/**
 * \brief Control schemes for motor control.
 * \details Enumerates the different control schemes available for PMSM motor control.
 */
typedef enum ControlScheme
{
    ControlScheme_speed = 0, /**< Speed control scheme. */
    ControlScheme_current,   /**< Current control scheme. */
    ControlScheme_torque,    /**< Torque control scheme. */
    ControlScheme_voltage    /**< Voltage control scheme. */
} ControlScheme;

